#include<stdio.h>
void main()
{
    int i,j,k;
    for(i=1;i<=10;i++)
    {
        for(j=i;j<11;j++)
        {
            printf(" ");
        }
        for(k=1;k<=i;k++)
        {
            printf("*");
        }
        for(k=2;k<=i;k++)
        {
            printf("*");
        }
        printf("\n");
    }
    for(i=0;i<=10;i++)
    {
      for(j=0;j<i;j++)
        {
            printf(" ");
        }
        for(k=i;k<=10;k++)
        {
            printf("*");
        }
        for(j=9;j>=i;j--)
        {
            printf("*");
        }
        printf("\n");
    }
}
